  // Helper function to normalize and lookup district descriptions
  function getDescription(districtName) {
    if (!districtName) return null
    
    // Try exact match first
    if (DESCRIPTIONS[districtName]) {
      return DESCRIPTIONS[districtName]
    }
    
    // Try title case normalization: "LUDHIANA" â "Ludhiana"
    const titleCase = districtName
      .toLowerCase()
      .split(' ')
      .map(word => word.charAt(0).toUpperCase() + word.slice(1))
      .join(' ')
    
    if (DESCRIPTIONS[titleCase]) {
      return DESCRIPTIONS[titleCase]
    }
    
    // Try direct uppercase match (in case key is stored as uppercase)
    const upperCase = districtName.toUpperCase()
    for (const key in DESCRIPTIONS) {
      if (key.toUpperCase() === upperCase) {
        return DESCRIPTIONS[key]
      }
    }
    
    return null
  }

